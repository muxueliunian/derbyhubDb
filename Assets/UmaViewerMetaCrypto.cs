namespace derbyhubDb.Assets;

public static class UmaViewerMetaCrypto
{
    private static readonly byte[] DbBaseKey =
    [
        0xF1, 0x70, 0xCE, 0xA4, 0xDF, 0xCE, 0xA3, 0xE1,
        0xA5, 0xD8, 0xC7, 0x0B, 0xD1, 0x00, 0x00, 0x00
    ];

    private static readonly byte[] DbKey =
    [
        0x6D, 0x5B, 0x65, 0x33, 0x63, 0x36,
        0x63, 0x25, 0x54, 0x71, 0x2D, 0x73,
        0x50, 0x53, 0x63, 0x38, 0x6D, 0x34,
        0x37, 0x7B, 0x35, 0x63, 0x70, 0x23,
        0x37, 0x34, 0x53, 0x29, 0x73, 0x43,
        0x36, 0x33
    ];

    public static byte[] BuildJapaneseMetaKey()
    {
        var key = DbKey.ToArray();
        for (var i = 0; i < key.Length; i++)
        {
            key[i] = (byte)(key[i] ^ DbBaseKey[i % 13]);
        }

        return key;
    }
}
